"""
Regenerate github_pages/data/samples_<sport>.js for each sport from the
matching human_study_samples_<sport>_subset.json.
"""

import json
import os

HERE = os.path.dirname(os.path.abspath(__file__))

SPORTS = ["basketball", "hockey", "soccer"]


def main():
    for sport in SPORTS:
        src = os.path.join(HERE, "..", f"human_study_samples_{sport}_subset.json")
        dst = os.path.join(HERE, "data", f"samples_{sport}.js")
        if not os.path.exists(src):
            print(f"  skip {sport}: {src} not found")
            continue

        with open(src) as f:
            samples = json.load(f)

        with open(dst, "w") as f:
            f.write(f"// Auto-generated by build_data.py — do not edit by hand. ({sport})\n")
            f.write("window.SAMPLES = ")
            json.dump(samples, f, indent=2, ensure_ascii=False)
            f.write(";\n")

        print(f"Wrote {len(samples)} samples ({sport}) -> {dst}")


if __name__ == "__main__":
    main()
